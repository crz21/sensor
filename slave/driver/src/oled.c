/* 发送命令 */
void oled_send_cmd(uint8_t o_command)
{
    IIC_Start();
    IIC_Send_Byte(DEV_OLED, 0x78);
    IIC_Send_Byte(DEV_OLED, 0x00); 
    IIC_Send_Byte(DEV_OLED, o_command);
    IIC_Stop();
}

/* 发送数据 */
void oled_send_data(uint8_t o_data)
{ 
    IIC_Start();
    IIC_Send_Byte(DEV_OLED, 0x78);
    IIC_Send_Byte(DEV_OLED, 0x40);
    IIC_Send_Byte(DEV_OLED, o_data);
    IIC_Stop();
}

/* 列设置 */
void column_set(uint8_t column)
{
    oled_send_cmd(0x10 | (column >> 4));    //设置列地址高位
    oled_send_cmd(0x00 | (column & 0x0f));   //设置列地址低位
}

/* 行设置 */
void page_set(uint8_t page)
{
    oled_send_cmd(0xb0 + page);
}

/* 清屏操作 */
void oled_clear(void)
{
    uint8_t page, column;

    for (page = 0; page < (MAX_PAGE / 8); page++) //page loop
    {            
        page_set(page);
        column_set(0);
        for (column = 0; column < MAX_COLUMN; column++)//column loop
        { 
            oled_send_data(0x00);
        }
    }
}

/* 满屏操作 */
void oled_full(void)
{
    uint8_t page, column;

    for (page = 0; page < (MAX_PAGE / 8); page++)//page loop
    {             
        page_set(page);
        column_set(0);
        for (column = 0; column < MAX_COLUMN; column++)//column loop
        {   
            oled_send_data(0xff);
        }
    }
}

/* 按行写字 */
void oled_part(const uint8_t *ptr_word)
{
    uint8_t page, column, column_word, i;

    for (i = 0; i < 4; i++)
    {
        for (column_word = 0; column_word < 2; column_word++)
        {
            for (page = (2 * i); page < (2 * i + 2); page++)
            {
                page_set(page);                     //决定所处行位置
                column_set(16 * column_word);       //决定所处列位置
                for (column = 0; column < 16; column++)
                {
                    oled_send_data(*ptr_word++);
                }
            }
        }
        i++;
    }
}

void oled_half_part(uint8_t page_num, uint8_t column_word_num, uint8_t *ptr_word)
{
    uint8_t page, column, column_word;

    for (column_word = 0; column_word < column_word_num; column_word++) 
    {
        for (page = page_num; page < (page_num + 2); page++) 
        {
            page_set(page);                     //决定所处行位置
            column_set(8 * column_word + 48);       //决定所处列位置

            for (column = 0; column < 8; column++) 
            {
                oled_send_data(*ptr_word++);
            }
        }
    }

}

void oled_title(const uint8_t *ptr_word)
{
    uint8_t page, column, column_word;

    for (column_word = 0; column_word < MAX_COLUMN_WORDS; column_word++) 
    {
        for (page = 0; page < 2; page++) //page loop
        { 
            page_set(page);
            column_set(16 * column_word);
					
            for (column = 0; column < 16; column++)//column loop
            { 			
                oled_send_data(*ptr_word++);
            }
        }
    }
}


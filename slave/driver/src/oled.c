/* 显示图片 */
void picture_display(const uint8_t *ptr_pic)
{
    uint8_t page, column;

    for (page = 0; page < (MAX_PAGE / 8); page++)//page loop
    {        
        page_set(page);
        column_set(0);
        for (column = 0; column < MAX_COLUMN; column++)//column loop
        {   
            oled_send_data(*ptr_pic++);
        }
    }
}

/* 反显图片 */
void picture_reverse_display(const uint8_t *ptr_pic)
{
    uint8_t page, column, data;

    for (page = 0; page < (MAX_PAGE / 8); page++) //page loop
    {        
        page_set(page);
        column_set(0);
        for (column = 0; column < MAX_COLUMN; column++) //column loop
        {   
            data = *ptr_pic++;
            data = ~data;
            oled_send_data(data);
        }
    }
}

